function WebController() {
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
     继承于Controller基类
     -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    Controller.call(this);
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
     bindEvent
     -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    this.bindEvent();
};
/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
绑定事件
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
WebController.prototype.bindEvent = function() {
    var classSelf = this;
    $('.operation').on('click', function(event) {
        event.preventDefault();
        /* Act on the event */
        var type = $(this).data('type');
        var activityId = $('#activity_id').val();
        if (type === 'play') {
            var newWindow = window.open();
            newWindow.location.href = "http://v.youku.com/v_show/id_XMTYyNzYyMTg5Ng==.html?from=s1.8-1-1.2";
        } else if (type === 'desc') {
            $('.sec_desc').show();
            $('.sec_rule').hide();
            $('.sec_summary').hide();
        } else if (type === 'rule') {
            $('.sec_desc').hide();
            $('.sec_rule').show();
            $('.sec_summary').hide();
        } else if (type === 'summary') {
            $('.sec_desc').hide();
            $('.sec_rule').hide();
            $('.sec_summary').show();
        }
    });
};
/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
类的初始化
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
$(document).ready(function() {
    new WebController;
});
